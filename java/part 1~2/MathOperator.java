public class MathOperator{
    public static void main(String args[]){
        int a = 13;
        int b = 4;
        
        System.out.println("a="+a+",b="+b);
        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
        System.out.println("a/b="+((float)a/b));
        System.out.println(a+"%"+b+"="+(a%b));
        System.out.println(b+"%"+a+"="+(b%a));

   }
}
